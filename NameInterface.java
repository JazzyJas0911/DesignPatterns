package CECS277Lab9;

/*
 * interface called NameInterface
 */
public interface NameInterface{
	
	/**
	 * abstract method to set name
	 * @param n - name
	 */
	public abstract void setName(String n);
	
	/**
	 * abstract method to get name
	 * @return name
	 */
	public abstract String getName();

}

